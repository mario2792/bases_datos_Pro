-- DROP TABLE IF EXISTS USUARIO;
CREATE TABLE USUARIO(
ID_USUARIO varchar(12) primary key not null unique,
N_USUARIO varchar(12) not null,
DIRECCION varchar(200) not null,
CONTASENA varchar(12) not null ,
EMAIL varchar(100) not null,
NOMBRE varchar(50) not null,
APELLIDO varchar(12) not null,
TIPO varchar(12) not null CHECK(TIPO="ESTUDIANTE" OR TIPO="DOCENTE"),
APELLIDO_MATERNO varchar(20) not null
);
-- DROP TABLE ALUMNO;
CREATE TABLE ALUMNO(
MATRICULA varchar(12) primary key not null unique,
CALIFICACION varchar(12) not null,
ID_USUARIO varchar(12) not null ,
ID_DOCENTE varchar(12)  not null , 
FOREIGN KEY (ID_USUARIO) REFERENCES USUARIO(ID_USUARIO)

);

CREATE TABLE NOTA(
CODNOTA varchar(12) primary key not null unique,
CURSO varchar(12) not null ,
CALIFICACION FLOAT DEFAULT 0 , 
OBSERVACION TEXT 
);

CREATE TABLE TAREA(
CODASIGNATURA varchar(12)  not null ,
CODNOTA varchar(12)  not null ,
CURSO FLOAT DEFAULT 0 , 
DESCRIPCION varchar(12) not null ,
FECHA date,
PRIMARY KEY(CODASIGNATURA,CODNOTA),
FOREIGN KEY (CODNOTA) REFERENCES NOTA(CODNOTA)
);

-- DROP TABLE ASIGNATURA;
CREATE TABLE ASIGNATURA(
CODASIGNATURA varchar(12) primary key ,
ASIGNATURA varchar(12) NOT NULL,
MATRICULA varchar(12) NOT NULL, 
FOREIGN KEY (CODASIGNATURA) REFERENCES TAREA(CODASIGNATURA),
FOREIGN KEY (MATRICULA) REFERENCES ALUMNO(MATRICULA)
);

-- DROP TABLE CURSO;
CREATE TABLE CURSO (
ID_CURSO VARCHAR(12) PRIMARY KEY NOT NULL,
ID_DOCENTE VARCHAR(12) NOT NULL,
MATRICULA VARCHAR(12) NOT NULL,
foreign key (MATRICULA) REFERENCES ALUMNO(MATRICULA)

);

-- DROP TABLE DOCENTE;
CREATE TABLE DOCENTE(
ID_DOCENTE varchar(12) primary key not null unique,
CODASIGNATURA varchar(12) not null ,
ID_CURSO varchar(12)  not null , 
IDREPRESENTANTE varchar(12)  not null ,
FOREIGN KEY (ID_DOCENTE) REFERENCES ASIGNATURA(CODASIGNATURA),
foreign key (ID_CURSO) REFERENCES CURSO(ID_CURSO)
);



-- DROP TABLE REPRESENTANTE;
CREATE TABLE REPRESENTANTE(
IDREPRESENTANTE VARCHAR(12) NOT NULL primary key UNIQUE ,
NOMBRE varchar(50) not null,
APELLIDO varchar(20) not null,
TELEFONO varchar(20) not null ,
COREO VARchar(50) not null,
matricula VARchar(12) not null,
foreign key (matricula) references ALUMNO(MATRICULA)
);
-- DROP TABLE ASISTENCIA;
CREATE TABLE ASISTENCIA(
IDFALTA VARCHAR(12) PRIMARY KEY NOT NULL,
FECHA DATE,
JUSTIFICADA BOOLEAN,
FOREIGN KEY (IDFALTA) REFERENCES ALUMNO(MATRICULA)
);

